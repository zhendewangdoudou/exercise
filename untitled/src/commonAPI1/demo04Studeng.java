package commonAPI1;

public class demo04Studeng {

    private String name;
    private int age;

    public demo04Studeng() {
    }

    public demo04Studeng(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
