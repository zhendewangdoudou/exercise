package API2.leiObject.Objectlei;

public class demo02person {

    private String name;
    private int age;

    public demo02person() {
    }

    public demo02person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
